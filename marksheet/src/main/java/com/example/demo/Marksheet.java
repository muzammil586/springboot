package com.example.demo;

public class Marksheet {
	
	private int math;
	private int eng;
	private int sci;
	private int isl;
	private int bio;
	public int getMath() {
		return math;
	}
	public void setMath(int math) {
		this.math = math;
	}
	public int getEng() {
		return eng;
	}
	public void setEng(int eng) {
		this.eng = eng;
	}
	public int getSci() {
		return sci;
	}
	public void setSci(int sci) {
		this.sci = sci;
	}
	public int getIsl() {
		return isl;
	}
	public void setIsl(int isl) {
		this.isl = isl;
	}
	public int getBio() {
		return bio;
	}
	public void setBio(int bio) {
		this.bio = bio;
	}
		

	
}
